CREATE TABLE DEBTORS (
    DEBTOR_ID UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    DEBTOR_CPF VARCHAR(14),
    EMAIL VARCHAR(255),
    NAME VARCHAR(255),
    ADDRESS VARCHAR(255),
    STATUS BOOLEAN,
    PHONE_NUMBER VARCHAR(20)
);

CREATE TABLE DEBTS (
    DEBT_ID UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    DEBTOR_ID UUID REFERENCES DEBTORS(DEBTOR_ID) ON DELETE NO ACTION,
    DESCRIPTION VARCHAR(255),
    AMOUNT DECIMAL(10,2),
    CREATED_DATE TIMESTAMP,
    DUE_DATE DATE,
    STATUS VARCHAR(20)
);

CREATE TABLE PAYMENTS (
    PAYMENT_ID UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    DEBT_ID UUID REFERENCES DEBTS(DEBT_ID) ON DELETE NO ACTION,
    AMOUNT DECIMAL(10,2),
    PAYMENT_DATE TIMESTAMP,
    METHOD VARCHAR(50)
);

